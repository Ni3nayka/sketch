byte mas[ras], pyt[ras], t_start, t_finish, t_nast, ii, gg;

void navigator() {
  ii = 1;
  while (ii < ras) {
    mas[ii] = 999;
    ii++;
  }
  //t_finish = 15;
  //t_start = 11;           -----------------------------
  ii = 1;
  t_nast = t_start; mas[t_nast] = 0;
  gg = 1;
  while (gg > 0) {
    gg = 0;
    while (t_nast < ras) {
      while (ii < ras) {
        if ((dor[t_nast][ii] > 0) && (mas[ii] > mas[t_nast] + dor[t_nast][ii])) {
          mas[ii] = mas[t_nast] + dor[t_nast][ii];
          gg++;
        }
        ii++;
      }
      ii = 1;
      t_nast++;
    }
    t_nast = 1;
  }
  //----------------------------------------------------
  t_nast = t_finish;
  pyt[1] = t_finish;
  ii = 1;
  gg = 2;
  while (t_nast != t_start) {
    while (dor[t_nast][ii] == 0) { ii++; }
    if ((dor[t_nast][ii] + mas[ii]) == mas[t_nast]) {
      t_nast = ii;
      ii = 1;
      pyt[gg] = t_nast;
      gg++;
    }
    else {
      ii++;
    }
  }
  t_nast = t_finish;
  pyt[1] = t_finish;
  ii = 1;
  gg = 2;
  while (t_nast != t_start) {
    while (dor[t_nast][ii] == 0) { ii++; }
    if ((dor[t_nast][ii] + mas[ii]) == mas[t_nast]) {
      t_nast = ii;
      ii = 1;
      pyt[gg] = t_nast;
      gg++;
    }
    else {
      ii++;
    }
  }
  ii = 1; gg = 1;
  while (pyt[gg] != 0) { gg++; }
  gg--;
  if (gg%2 == 0) {
    while ((gg - ii) >= 1) {
      t_nast = pyt[ii];
      pyt[ii] = pyt[gg];
      pyt[gg] = t_nast;
      ii++;
      gg--;
    }
  }
  else {
    while ((gg - ii) >= 2) {
      t_nast = pyt[ii];
      pyt[ii] = pyt[gg];
      pyt[gg] = t_nast;
      ii++;
      gg--;
    }
  }
}
